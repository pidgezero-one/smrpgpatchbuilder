        output += "\n\nactions = ActionScriptBank([\n"
        for i, script in enumerate(a_scripts):
            output += f"\tscript_{i},\n"
        output += "])"
        writeline(file, output)
        file.close()

        open(f"{output_path}/event/__init__.py", "w")
        open(f"{event_path}/__init__.py", "w")
